import { writeFileSync } from 'fs';
import { computeXgRatings } from '../model/xgRatings.js';
import { applyEloPriors } from '../model/elo.js';
import { fitRho, type TrainingMatch } from '../model/dixonColes.js';
import { expectedGoals } from '../model/poisson.js';
import { runBacktest, type BacktestPrediction } from '../model/backtest.js';
import { fitCalibration } from '../model/calibration.js';
import { prisma } from '../db.js';

const LEAGUES = ['EPL', 'La Liga', 'Bundesliga', 'Serie A'];

function brierScore(pairs: Array<{ p: number; y: number }>): number {
  if (pairs.length === 0) return NaN;
  return pairs.reduce((s, x) => s + (x.p - x.y) ** 2, 0) / pairs.length;
}

function reliabilityBins(preds: BacktestPrediction[], market: 'h2h' | 'over25' | 'btts') {
  const bins = Array.from({ length: 10 }, (_, i) => ({
    lo: i * 0.1,
    hi: (i + 1) * 0.1,
    sumPred: 0,
    sumAct: 0,
    n: 0,
  }));

  for (const p of preds) {
    let pred: number, actual: boolean;
    if (market === 'h2h') { pred = p.pHome; actual = p.actualHome; }
    else if (market === 'over25') { pred = p.pOver25; actual = p.actualOver25; }
    else { pred = p.pBtts; actual = p.actualBtts; }

    const idx = Math.min(9, Math.floor(pred * 10));
    bins[idx]!.sumPred += pred;
    bins[idx]!.sumAct += actual ? 1 : 0;
    bins[idx]!.n++;
  }

  return bins.filter((b) => b.n > 0).map((b) => ({
    range: `${(b.lo * 100).toFixed(0)}-${(b.hi * 100).toFixed(0)}%`,
    n: b.n,
    avgPred: b.sumPred / b.n,
    avgAct: b.sumAct / b.n,
  }));
}

async function main() {
  const lines: string[] = [];
  const ts = new Date().toISOString().slice(0, 10);

  lines.push(`# Model Report — ${ts}`);
  lines.push('');
  lines.push('> Auto-generated by `generateModelReport.ts`. Do not edit manually.');
  lines.push('');

  for (const league of LEAGUES) {
    lines.push(`## ${league}`);
    lines.push('');

    // --- Ratings ---
    const lr = await computeXgRatings(league);
    await applyEloPriors(league, lr.teams);

    const teams = [...lr.teams.values()].filter((t) => t.games >= 5).sort((a, b) => b.attack - a.attack);

    lines.push(`**League avg:** ${lr.leagueAvgAttack.toFixed(3)} goals/game | **Teams rated:** ${teams.length}`);
    lines.push('');

    if (teams.length > 0) {
      lines.push('### Top 3 / Bottom 3 Attack');
      lines.push('');
      lines.push('| Team | Attack | Defense | Games |');
      lines.push('|------|--------|---------|-------|');
      const top3 = teams.slice(0, 3);
      const bot3 = teams.slice(-3).reverse();
      for (const t of [...top3, { team: '—', attack: NaN, defense: NaN, games: 0 }, ...bot3]) {
        if (isNaN(t.attack)) {
          lines.push('| **↑ top / bottom ↓** | | | |');
        } else {
          lines.push(`| ${t.team} | ${t.attack.toFixed(3)} | ${t.defense.toFixed(3)} | ${t.games} |`);
        }
      }
      lines.push('');
    }

    // --- Fitted ρ ---
    const matches = await prisma.match.findMany({
      where: { league, status: 'FINAL', homeGoals: { not: null } },
      orderBy: { kickoffUtc: 'asc' },
    });

    const training: TrainingMatch[] = [];
    for (const m of matches) {
      const home = lr.teams.get(m.homeTeam);
      const away = lr.teams.get(m.awayTeam);
      if (!home || !away) continue;
      const { lambdaHome, lambdaAway } = expectedGoals(
        home.attack, home.defense, away.attack, away.defense, lr.leagueAvgAttack,
      );
      training.push({ lambdaHome, lambdaAway, homeGoals: m.homeGoals!, awayGoals: m.awayGoals! });
    }

    if (training.length >= 50) {
      const fit = fitRho(training);
      lines.push(`**Dixon-Coles ρ:** ${fit.rho.toFixed(4)}  (LL improvement: ${fit.improvement.toFixed(2)}, n=${fit.nMatches})`);
      lines.push('');
    }

    // --- Backtest + Calibration ---
    const preds = await runBacktest(league);

    if (preds.length === 0) {
      lines.push('_Insufficient data for backtest._');
      lines.push('');
      continue;
    }

    const markets: Array<{ name: string; key: 'h2h' | 'over25' | 'btts'; pred: (p: BacktestPrediction) => number; act: (p: BacktestPrediction) => number }> = [
      { name: 'Home win', key: 'h2h', pred: (p) => p.pHome, act: (p) => p.actualHome ? 1 : 0 },
      { name: 'Draw',     key: 'h2h', pred: (p) => p.pDraw, act: (p) => p.actualDraw ? 1 : 0 },
      { name: 'Away win', key: 'h2h', pred: (p) => p.pAway, act: (p) => p.actualAway ? 1 : 0 },
      { name: 'Over 2.5', key: 'over25', pred: (p) => p.pOver25, act: (p) => p.actualOver25 ? 1 : 0 },
      { name: 'BTTS',     key: 'btts',   pred: (p) => p.pBtts,   act: (p) => p.actualBtts ? 1 : 0 },
    ];

    lines.push(`### Backtest (n=${preds.length} held-out matches)`);
    lines.push('');
    lines.push('| Market | Brier (raw) | Brier (calibrated) | Improvement | Calibrated? |');
    lines.push('|--------|-------------|---------------------|-------------|-------------|');

    for (const mkt of markets) {
      const pairs = preds.map((p) => ({ predicted: mkt.pred(p), actual: mkt.act(p) }));
      const cal = fitCalibration(league, mkt.name, pairs);
      const delta = cal.brierBefore - cal.brierAfter;
      lines.push(
        `| ${mkt.name} | ${cal.brierBefore.toFixed(4)} | ${cal.brierAfter.toFixed(4)} | ${delta >= 0 ? '+' : ''}${(delta * 1000).toFixed(1)}‰ | ${cal.fitted ? '✓' : 'identity'} |`,
      );
    }

    lines.push('');

    // Reliability bins for Home win and Over 2.5
    for (const [label, key] of [['Home win', 'h2h'], ['Over 2.5', 'over25']] as const) {
      const bins = reliabilityBins(preds, key);
      lines.push(`### Reliability diagram — ${label}`);
      lines.push('');
      lines.push('| Predicted range | n | Avg predicted | Avg actual |');
      lines.push('|-----------------|---|---------------|------------|');
      for (const b of bins) {
        const flag = Math.abs(b.avgPred - b.avgAct) > 0.08 ? ' ⚠️' : '';
        lines.push(`| ${b.range} | ${b.n} | ${(b.avgPred * 100).toFixed(1)}% | ${(b.avgAct * 100).toFixed(1)}%${flag} |`);
      }
      lines.push('');
    }
  }

  lines.push('---');
  lines.push(`_Generated: ${new Date().toISOString()}_`);

  const reportPath = '/Users/folyananou/Documents/fananou/betting-system-v2/model-report.md';
  writeFileSync(reportPath, lines.join('\n'));
  console.log(`\n✓ Report written to ${reportPath}`);

  await prisma.$disconnect();
}
main().catch((e) => { console.error(e); process.exit(1); });
